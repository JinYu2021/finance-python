from pulp import *
def find_subset_pulp(numbers, target):
    """
    使用PuLP库解决子集和问题，找到一组数字使其和等于目标值

    参数:
    numbers: 数字列表
    target: 目标和

    返回:
    包含满足条件的数字组合的列表
    """
    # 创建问题实例
    prob = LpProblem("SubsetSum", LpMinimize)

    # 创建二进制变量（每个数字是否被选择）
    choices = LpVariable.dicts("Choice", range(len(numbers)), 0, 1, LpInteger)

    # 目标函数（最小化选择的数字个数）
    prob += lpSum([choices[i] for i in range(len(numbers))])

    # 约束条件：选择的数字之和等于目标值
    prob += lpSum([numbers[i] * choices[i] for i in range(len(numbers))]) == target

    try:
        # 求解（使用CBC求解器，msg=0表示不显示求解过程）
        prob.solve(PULP_CBC_CMD(msg=0))

        # 检查求解状态
        if LpStatus[prob.status] == "Optimal":
            selected = [numbers[i] for i in range(len(numbers)) if value(choices[i]) == 1]
            return [selected]
        else:
            return []
    except:
        return []


